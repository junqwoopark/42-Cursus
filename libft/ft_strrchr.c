#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ret;

	ret = 0;
	while (*s)
	{
		if (*s == (char)c)
			ret = (char *) s;
		s++;
	}
	if (*s == (char) c)
		return ((char *) s);
	return (ret);
}
